function soma (a, b) {
  return a + b
}

function sub(a, b) {
    return a - b;
}

function mult(a, b) {       
    return a * b;
}   

function div(a, b) {
  return a / b;
}

let resultado1 = soma(6, 2);
let resultado2 = sub(6, 2);
let resultado3 = mult(6, 2);
let resultado4 = div(6, 2);

console.log(resultado1);
console.log(resultado2);
console.log(resultado3);
console.log(resultado4);

